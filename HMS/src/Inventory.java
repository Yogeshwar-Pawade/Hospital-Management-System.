    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int roomno = (Integer)jSpinner1.getValue();
            String room = Integer.toString(roomno);
            conn con = new conn();
            String sql = "insert into inventory(pno, drugcode, drugname, quantity, price, total) values(?,?,?,?,?,?)";
            PreparedStatement st = con.c.prepareStatement(sql);
            
            st.setString(1, lblprescriptionid.getText());
            st.setString(2, txtdrugcode.getText());
            st.setString(3, txtdrugname.getText());
            st.setString(4, room);
            st.setString(5, txtprice.getText());
            st.setString(6, txttotalcost.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Added Successfully");
            table();
            id();
            } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

