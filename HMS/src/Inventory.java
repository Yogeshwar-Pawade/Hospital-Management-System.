    public void id(){
        try {
            conn con = new conn();
            String sql = "select max(pno) from inventory";
            PreparedStatement st = con.c.prepareStatement(sql);
            ResultSet rs = st.executeQuery();
            rs.next();
            rs.getString("max(pno)");
            if (rs.getString("max(pno)") == null) {
                lblprescriptionid.setText("1");     
            } else {
                int id = rs.getByte("max(pno)");
                id++;
                String ID = Long.toString(id);
                lblprescriptionid.setText(ID);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    public void table(){
        int CC;
        try {
            conn con = new conn();
            String sql = "select * from inventory";
            PreparedStatement st = con.c.prepareStatement(sql);
            ResultSet Rs = st.executeQuery();  
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("pno"));
                    v2.add(Rs.getString("drugcode"));
                    v2.add(Rs.getString("drugname"));
                    v2.add(Rs.getString("quantity"));
                    v2.add(Rs.getString("price"));
                    v2.add(Rs.getString("total"));
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
        }
    }
    
